phrase = "Giraffe Academy"

# len()
print(len(phrase))

print(phrase[0])
print(phrase[8])

# lower()
print(phrase.lower())

# upper()
print(phrase.upper())

# islower()
print(phrase.lower().islower())

# isupper()
print(phrase.upper().isupper())

# index()
print(phrase.index("G"))
print(phrase.index("Academy"))

# replace()
print(phrase.replace("Giraffe", "Elephant"))
