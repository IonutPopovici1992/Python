from math import *


# Arithmetic Operators:
# Addition: 3 + 2
print(3 + 2)
# Subtraction: 3 - 2
print(3 - 2)
# Multiplication: 3 * 2
print(3 * 2)
# Division: 3 / 2
print(3 / 2)
# Floor Division: 3 // 2
print(3 // 2)
# Exponent: 3 ** 2
print(3 ** 2)
# Modulus: 3 % 2
print(3 % 2)


# Comparisons:
# Equal: 3 == 2
print(3 == 2)
# Not Equal: 3 != 2
print(3 != 2)
# Greater Than: 3 > 2
print(3 > 2)
# Less Than: 3 < 2
print(3 < 2)
# Greater or Equal: 3 >= 2
print(3 >= 2)
# Less or Equal: 3 <= 2
print(3 <= 2)


# abs()
print(abs(-5))
# round()
print(round(3.14))
print(round(3.75))
# floor()
print(floor(3.5))


